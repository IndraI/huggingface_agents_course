# --- Enhanced Agent Definition ---
class BasicAgent:
    def __init__(self):
        self.model = OpenAIServerModel(
            model_id="gpt-4o-mini",
            max_tokens=600  # Increased for more complex answers
        )
        self.agent = CodeAgent(
            tools=[DuckDuckGoSearchTool(), WikipediaSearchTool()],
            model=self.model,
            additional_authorized_imports=['re', 'json', 'datetime'],
            max_steps=4  # Balanced for multi-step questions
        )

    def answer(self, question):
        # Direct answer for reversed text question
        if question.startswith(".rewsna"):
            # The reversed question asks: "If you understand this sentence, write the opposite of the word 'left' as the answer"
            return "right"
        
        # Enhanced instruction for better accuracy
        instruction = """
You are an expert assistant solving questions using tools and reasoning.

IMPORTANT RULES:
1. For Wikipedia questions: Use WikipediaSearchTool to search specific topics
2. For web/current info: Use DuckDuckGoSearchTool
3. For math/logic tables: Write and execute Python code directly
4. For multi-step questions: Break down into steps, use tools for each step
5. Return ONLY the exact answer requested (no extra explanations unless asked)
6. Pay attention to the exact format requested (e.g., "first name only", "comma separated list", "IOC code")

Examples of good answers:
- "How many albums..." → Return just the number like "5"
- "Give only the first name" → Return just "John" not "John Smith"
- "List as comma separated" → Return "item1, item2, item3"
- "Give the IOC country code" → Return "USA" not "United States"

For botanical classification questions: Remember that botanically, many "vegetables" are actually fruits (tomatoes, peppers, cucumbers, etc.)

For table/math problems: Write Python code to solve them systematically.
"""
        
        prompt = f"{instruction}\n\nQuestion: {question}\n\nSolve step-by-step and provide the exact answer in the format requested."
        
        try:
            res = self.agent.run(prompt)
            answer = str(res() if callable(res) else res)
            
            # Clean up common issues
            answer = answer.strip()
            
            # Remove common prefixes that agents add
            prefixes_to_remove = [
                "The answer is: ",
                "Answer: ",
                "Final answer: ",
                "The final answer is: "
            ]
            for prefix in prefixes_to_remove:
                if answer.startswith(prefix):
                    answer = answer[len(prefix):].strip()
            
            return answer
        except Exception as e:
            print(f"Agent error on question: {e}")
            return f"Unable to determine answer"

    def __call__(self, question: str) -> str:
        return self.answer(question)

def run_and_submit_all( profile: gr.OAuthProfile | None):
    """
    Fetches all questions, runs the BasicAgent on them, submits all answers,
    and displays the results.
    """
    # --- Determine HF Space Runtime URL and Repo URL ---
    space_id = os.getenv("SPACE_ID") # Get the SPACE_ID for sending link to the code

    if profile:
        username= f"{profile.username}"
        print(f"User logged in: {username}")
    else:
        print("User not logged in.")
        return "Please Login to Hugging Face with the button.", None

    api_url = DEFAULT_API_URL
    questions_url = f"{api_url}/questions"
    submit_url = f"{api_url}/submit"

    # 1. Instantiate Agent ( modify this part to create your agent)
    try:
        agent = BasicAgent()
    except Exception as e:
        print(f"Error instantiating agent: {e}")
        return f"Error initializing agent: {e}", None
    # In the case of an app running as a hugging Face space, this link points toward your codebase ( usefull for others so please keep it public)
    agent_code = f"https://huggingface.co/spaces/{space_id}/tree/main"
    print(agent_code)

    # 2. Fetch Questions
    print(f"Fetching questions from: {questions_url}")
    try:
        response = requests.get(questions_url, timeout=15)
        response.raise_for_status()
        questions_data = response.json()
        if not questions_data:
             print("Fetched questions list is empty.")
             return "Fetched questions list is empty or invalid format.", None
        print(f"Fetched {len(questions_data)} questions.")
    except requests.exceptions.RequestException as e:
        print(f"Error fetching questions: {e}")
        return f"Error fetching questions: {e}", None
    except requests.exceptions.JSONDecodeError as e:
         print(f"Error decoding JSON response from questions endpoint: {e}")
         print(f"Response text: {response.text[:500]}")
         return f"Error decoding server response for questions: {e}", None
    except Exception as e:
        print(f"An unexpected error occurred fetching questions: {e}")
        return f"An unexpected error occurred fetching questions: {e}", None

    # 3. Run your Agent
    results_log = []
    answers_payload = []
    print(f"Running agent on {len(questions_data)} questions...")
    for item in questions_data:
        task_id = item.get("task_id")
        question_text = item.get("question")
        if not task_id or question_text is None:
            print(f"Skipping item with missing task_id or question: {item}")
            continue
        try:
            submitted_answer = agent(question_text)
            answers_payload.append({"task_id": task_id, "submitted_answer": submitted_answer})
            results_log.append({"Task ID": task_id, "Question": question_text, "Submitted Answer": submitted_answer})
        except Exception as e:
             print(f"Error running agent on task {task_id}: {e}")
             results_log.append({"Task ID": task_id, "Question": question_text, "Submitted Answer": f"AGENT ERROR: {e}"})

    if not answers_payload:
        print("Agent did not produce any answers to submit.")
        return "Agent did not produce any answers to submit.", pd.DataFrame(results_log)

    # 4. Prepare Submission 
    submission_data = {"username": username.strip(), "agent_code": agent_code, "answers": answers_payload}
    status_update = f"Agent finished. Submitting {len(answers_payload)} answers for user '{username}'..."
    print(status_update)

    # 5. Submit
    print(f"Submitting {len(answers_payload)} answers to: {submit_url}")
    try:
        response = requests.post(submit_url, json=submission_data, timeout=60)
        response.raise_for_status()
        result_data = response.json()
        final_status = (
            f"Submission Successful!\n"
            f"User: {result_data.get('username')}\n"
            f"Overall Score: {result_data.get('score', 'N/A')}% "
            f"({result_data.get('correct_count', '?')}/{result_data.get('total_attempted', '?')} correct)\n"
            f"Message: {result_data.get('message', 'No message received.')}"
        )
        print("Submission successful.")
        results_df = pd.DataFrame(results_log)
        return final_status, results_df
    except requests.exceptions.HTTPError as e:
        error_detail = f"Server responded with status {e.response.status_code}."
        try:
            error_json = e.response.json()
            error_detail += f" Detail: {error_json.get('detail', e.response.text)}"
        except requests.exceptions.JSONDecodeError:
            error_detail += f" Response: {e.response.text[:500]}"
        status_message = f"Submission Failed: {error_detail}"
        print(status_message)
        results_df = pd.DataFrame(results_log)
        return status_message, results_df
    except requests.exceptions.Timeout:
        status_message = "Submission Failed: The request timed out."
        print(status_message)
        results_df = pd.DataFrame(results_log)
        return status_message, results_df
    except requests.exceptions.RequestException as e:
        status_message = f"Submission Failed: Network error - {e}"
        print(status_message)
        results_df = pd.DataFrame(results_log)
        return status_message, results_df
    except Exception as e:
        status_message = f"An unexpected error occurred during submission: {e}"
        print(status_message)
        results_df = pd.DataFrame(results_log)
        return status_message, results_df


